// ── Build zone grid ──────────────────────────────────────────
function buildZones() {
  zonesWrap.innerHTML = '';
  zonesWrap.className = `zones-wrap p${playerCount}`;

  for (let i = 0; i < playerCount; i++) {
    const cfg  = PLAYER_CONFIG[i];
    const zone = document.createElement('div');
    zone.className = `zone ${cfg.cls}`;
    zone.dataset.player = i;

    // Header
    const header = document.createElement('div');
    header.className = 'zone-header';
    header.innerHTML = `
      <span class="zone-label">${cfg.label}</span>
      <span class="zone-score-chip" id="score-chip-${i}">0점</span>
    `;

    // Per-player repeated prompt (so each player sees the word clearly
    // in their own zone, in addition to the shared top-bar prompt)
    const zonePrompt = document.createElement('div');
    zonePrompt.className = 'zone-prompt';
    zonePrompt.id = `zone-prompt-${i}`;

    // 5-button color-choice grid
    const grid = document.createElement('div');
    grid.className = 'answer-grid';
    grid.id = `answer-grid-${i}`;

    zone.appendChild(header);
    zone.appendChild(zonePrompt);
    zone.appendChild(grid);
    zonesWrap.appendChild(zone);
  }
}

